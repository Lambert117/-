public class java03 {
    public static void main(String[] args) {

        //(3)java基本数据类型：字符、布尔

        //char 单字字符
        //注意：单引号
        //注意：只能存储一个字符
        char a = '你';
        System.out.println(a);

        //boolean 布尔类型
        //说明：布尔类型的数据只有两个true/false
        //true：真、成功
        //false：假、失败
        boolean b = true;
        System.out.println(b);
    }
}
