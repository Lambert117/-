public class java02 {
    public static void main(String[] args) {

        //(2)比较运算符6个
        //说明：运算结果都是布尔值true/false

        System.out.println(3 > 2);
        System.out.println(3 >= 2);
        System.out.println(3 < 2);
        System.out.println(3 <= 2);
        System.out.println(3 == 2);//相等
        System.out.println(3 != 2);

    }
}
